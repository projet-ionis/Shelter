    private void userSaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_userSaveButtonActionPerformed
        String firstName = firstNameField.getText();
        String lastName = lastNameField.getText();
        String password = passwordField.getText();
        Role role = null;
        if(roleField.getSelectedItem().toString().equalsIgnoreCase("administrator")){role = Role.ADMINISTRATOR;}
        else if(roleField.getSelectedItem().toString().equalsIgnoreCase("packer")){role = Role.PACKER;}
        else if(roleField.getSelectedItem().toString().equalsIgnoreCase("guest")){role = Role.GUEST;}
        else if(roleField.getSelectedItem().toString().equalsIgnoreCase("seller")){role = Role.SELLER;}
        try {
            client.addUser(firstName, lastName, role, password);
        } catch (RemoteException ex) {
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        newUserDialog.setVisible(false);
        try {
            userTable.setModel(getUsers());
        } catch (RemoteException ex) {
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_userSaveButtonActionPerformed

