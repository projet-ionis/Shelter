    private void stockSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stockSearchButtonActionPerformed
        String par = stockSearchField.getText();
        try {
            map = client.getStock();
        } catch (RemoteException ex) {
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultTableModel t = new DefaultTableModel();
        t.addColumn ((Object)"Article n°.");
        t.addColumn ((Object)"Nom");
        t.addColumn ((Object)"Quantité en stock");
        for(Object o : map.keySet()){
            Good g = (Good)o;
            if(g.getName().toLowerCase().contains(par.toLowerCase()) || Integer.toString(g.getArticleNr()).equalsIgnoreCase(par))
                    {
                t.addRow(new Object[] {g.getArticleNr(),g.getName(),g.getAmountReserved()});
            }
        }
        jTable2.setModel(t);
    }//GEN-LAST:event_stockSearchButtonActionPerformed

